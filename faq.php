    <script>
    (function(){
        // FAQ filter pills
        var pills = document.querySelectorAll('.faq-filter-pill');
        var items = document.querySelectorAll('.faq-item-v2[data-category]');
        pills.forEach(function(pill){
            pill.addEventListener('click', function(){
                pills.forEach(function(p){ p.classList.remove('active'); });
                pill.classList.add('active');
                var f = pill.getAttribute('data-filter');
                items.forEach(function(item){
                    item.style.display = (f === 'all' || item.getAttribute('data-category') === f) ? '' : 'none';
                });
            });
        });

        // Scroll-reveal words
        var words = document.querySelectorAll('.faq-reveal-word');
        var revealSection = document.querySelector('.faq-reveal-section');
        if (revealSection && words.length) {
            var observer = new IntersectionObserver(function(entries){
                entries.forEach(function(entry){
                    if (entry.isIntersecting) {
                        words.forEach(function(word, i){
                            setTimeout(function(){ word.classList.add('is-visible'); }, i * 100);
                        });
                        observer.unobserve(entry.target);
                    }
                });
            }, { threshold: 0.3 });
            observer.observe(revealSection);
        }

        // Spotlight mouse follow
        var grid = document.getElementById('spotlightGrid');
        if (grid) {
            var cards = grid.querySelectorAll('.faq-spotlight-card');
            cards.forEach(function(card){
                card.addEventListener('mousemove', function(e){
                    var rect = card.getBoundingClientRect();
                    var x = e.clientX - rect.left;
                    var y = e.clientY - rect.top;
                    var glow = card.querySelector('.faq-spotlight-glow');
                    if (glow) {
                        glow.style.left = x + 'px';
                        glow.style.top = y + 'px';
                        glow.style.transform = 'translate(-50%, -50%)';
                        glow.style.opacity = '1';
                    }
                });
                card.addEventListener('mouseleave', function(){
                    var glow = card.querySelector('.faq-spotlight-glow');
                    if (glow) glow.style.opacity = '0';
                });
            });
        }
    })();
    </script>

