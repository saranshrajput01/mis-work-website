    <script>
    (function(){
        // Filter pills
        var pills = document.querySelectorAll('.vg-filter-pill');
        var cards = document.querySelectorAll('.vg-spotlight-card[data-category]');
        pills.forEach(function(pill){
            pill.addEventListener('click', function(){
                pills.forEach(function(p){ p.classList.remove('active'); });
                pill.classList.add('active');
                var f = pill.getAttribute('data-filter');
                cards.forEach(function(card){
                    card.style.display = (f === 'all' || card.getAttribute('data-category') === f) ? '' : 'none';
                });
            });
        });

        // Scroll-reveal words
        var words = document.querySelectorAll('.vg-reveal-word');
        var revealSection = document.querySelector('.vg-reveal-section');
        if (revealSection && words.length) {
            var observer = new IntersectionObserver(function(entries){
                entries.forEach(function(entry){
                    if (entry.isIntersecting) {
                        words.forEach(function(word, i){
                            setTimeout(function(){ word.classList.add('is-visible'); }, i * 100);
                        });
                        observer.unobserve(entry.target);
                    }
                });
            }, { threshold: 0.3 });
            observer.observe(revealSection);
        }

        // Spotlight glow follow on video cards
        var grid = document.getElementById('vgSpotlightGrid');
        if (grid) {
            grid.querySelectorAll('.vg-spotlight-card').forEach(function(card){
                card.addEventListener('mousemove', function(e){
                    var rect = card.getBoundingClientRect();
                    var glow = card.querySelector('.vg-spotlight-glow');
                    if (glow) {
                        glow.style.left = (e.clientX - rect.left) + 'px';
                        glow.style.top = (e.clientY - rect.top) + 'px';
                        glow.style.transform = 'translate(-50%, -50%)';
                        glow.style.opacity = '1';
                    }
                });
                card.addEventListener('mouseleave', function(){
                    var glow = card.querySelector('.vg-spotlight-glow');
                    if (glow) glow.style.opacity = '0';
                });
            });
        }

        // Benefits glow follow
        var bGrid = document.getElementById('vgBenefitsGrid');
        if (bGrid) {
            bGrid.querySelectorAll('.vg-benefit-card').forEach(function(card){
                card.addEventListener('mousemove', function(e){
                    var rect = card.getBoundingClientRect();
                    var glow = card.querySelector('.vg-benefit-glow');
                    if (glow) {
                        glow.style.left = (e.clientX - rect.left) + 'px';
                        glow.style.top = (e.clientY - rect.top) + 'px';
                        glow.style.transform = 'translate(-50%, -50%)';
                        glow.style.opacity = '1';
                    }
                });
                card.addEventListener('mouseleave', function(){
                    var glow = card.querySelector('.vg-benefit-glow');
                    if (glow) glow.style.opacity = '0';
                });
            });
        }
    })();
